public class TesteTipoPrimitivosInteiros {
	public static void main(String[] args){
		
		int i = 10;
		System.out.println("int i = 10 => i = " + i);
		
		long l = 25661;
		System.out.println("long l = 25661 => l = " + l);
		
		long l2 = 22365656561l;
		System.out.println("long l2 = 22365656561l => l2 = " + l2);
		
		byte b = (byte) 123;
		System.out.println("byte b = (byte) 123 => " + b);
		
		byte b2 = (byte) 123;
		System.out.println("byte b2 = (byte) 123 => " + b2);
		
		short s = (short) 12565;
		System.out.println("short s = (short) 12565 => " + s);
		
		short s2 = (short) 1256568;
		System.out.println("short s2 = (short) 1256568 => " + s2);
		
	}
}