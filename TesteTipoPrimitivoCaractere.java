public class TesteTipoPrimitivoCaractere{
	public static void main(String[] args){
		char c = 'a';
		System.out.println("C = 'a' => " + c);
		
		char asc = 64;
		System.out.println("asc 64 => " + asc);
		
		char espaco = '\u0000';
		System.out.println("espaco = '\u0000' => " + espaco);
		
		char carinha = '\u0001';
		System.out.println("carinha = '\u0001' => " + carinha);
	}
}